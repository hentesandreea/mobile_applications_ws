// Return the maximum number out of 4 numbers.
function maxOutOf4(a, b, c, d) {
 return Math.max( a, b, c, d)

}
console.log(maxOutOf4(0, 1, 2, 3)) // Expected output: 3
console.log(maxOutOf4(1, 2, 3, 0)) // Expected output: 3
console.log(maxOutOf4(2, 3, 0, 1)) // Expected output: 3
console.log(maxOutOf4(3, 0, 1, 2)) // Expected output: 3
